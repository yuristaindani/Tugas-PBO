public class Menu {
    private String namaMenu;
    private double hargaMenu;

    public Menu(String namaMenu, int hargaMenu) {
        this.namaMenu = namaMenu;
        this.hargaMenu = hargaMenu;
    }


    public String getNamaMenu() {
        return namaMenu;
    }

    public double getHargaMenu() {
        return hargaMenu;
    }

}
